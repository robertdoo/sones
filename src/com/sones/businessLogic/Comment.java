public class Comment {
	private String id_;
	private String feedId_;
	
	public Comment(){
		
	}

	public String getId_() {
		return id_;
	}

	public void setId_(String id) {
		id_ = id;
	}

	public String getFeedId_() {
		return feedId_;
	}

	public void setFeedId_(String feedId) {
		feedId_ = feedId;
	}
	
	
}
